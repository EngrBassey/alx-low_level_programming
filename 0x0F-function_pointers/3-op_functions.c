#include "3-calc.h"

/**
  * op_add - function that adds two numbers
  * @a: first integer
  * @b: second integer
  * Return: return a + b
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - function that subtract two numbers
  * @a: first integer
  * @b: second integer
  * Return: return a - b
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - function that multiply two numbers
  * @a: first integer
  * @b: second integer
  * Return: return a * b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function that divid two numbers
  * @a: first integer
  * @b: second integer
  * Return: return a * b
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - function that multiply 2 numbers
  * @a: first integer
  * @b: second integer
  * Return: return a % b
  */

int op_mod(int a, int b)
{
	return (a / b);
}
