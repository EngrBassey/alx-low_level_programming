#include "3-calc.h"
#include <stdio.h>

/**
  * op_add - add function
  * @a: frist integer value
  * @b: second integer value
  * Return: return a and b
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - function to substract two values
  * @a: frist integer value
  * @b: second integer value
  * Return: return a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - function to multiply two values
  * @a: frist integer value
  * @b: second integer value
  * Return: return a and b
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function to divide two values
  * @a: frist integer value
  * @b: second integer value
  * Return: return a and b
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - function to find mode
  * @a: frist integer value
  * @b: second integer value
  * Return: return a and b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
